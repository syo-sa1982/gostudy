package main

type room struct {
	// 転送メッセージ
	forward chan []byte
	// 入室チャネル
	join chan *client
	// 退室チャネル
	leave chan *client
	// 在室中
	clients map[*client]bool
}

func (r *room) run() {
	for {
		select {
		case client := <-r.join:
			// 参加
			r.clients[client] = true
		case client := <-r.leave:

			delete(r.clients, client)
			close(client.send)
		case msg := <-r.forward:
			for client := range r.clients {
				select {
				case client.send <- msg:
				default:
					delete(r.clients, client)
					close(client.send)
				}
			}
		}
	}
}